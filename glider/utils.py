def generate_random_normalized_bboxes(batch_size, num_boxes):
    x_min = torch.rand((batch_size, num_boxes))
    y_min = torch.rand((batch_size, num_boxes))
    x_max = torch.rand((batch_size, num_boxes))
    y_max = torch.rand((batch_size, num_boxes))

    # Ensure x_min < x_max and y_min < y_max
    x_min, x_max = torch.min(x_min, x_max), torch.max(x_min, x_max)
    y_min, y_max = torch.min(y_min, y_max), torch.max(y_min, y_max)

    # Concatenate to form bounding boxes tensor
    bounding_boxes = torch.stack([x_min, y_min, x_max, y_max], dim=-1)

    return bounding_boxes